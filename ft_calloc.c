#include "libft.h"

void	*ft_calloc(size_t nmemb, size_t size)
{
	void	*res;

	if ((long long)size < 0 || (long long)nmemb <= 0)
		return (NULL);
	res = malloc(nmemb * size);
	if (!res)
		return (NULL);
	ft_bzero(res, nmemb * size);
	return (res);
}
