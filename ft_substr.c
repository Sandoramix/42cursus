#include "libft.h"

char	*ft_substr(char const *s, unsigned int start, size_t len)
{
	char	*res;
	int		s_len;
	int		end;

	s_len = ft_strlen(s);
	end = start + len;
	if (s_len < end)
		end = s_len;
	res = ft_calloc((end - start) + 1, sizeof(char *));
	ft_memcpy(res, &s[start],end - start);
	return (res);
}