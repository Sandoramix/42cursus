#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*s_p;

	i = -1;
	s_p = (unsigned char *)s;
	while (++i < n)
		if (s_p[i] == (unsigned char)c)
			return (&s_p[i]);
	return (NULL);
}
