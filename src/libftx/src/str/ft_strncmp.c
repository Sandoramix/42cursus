#include "libft.h"

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	size_t			i;
	unsigned char	*s1p;
	unsigned char	*s2p;

	s1p = (unsigned char *)s1;
	s2p = (unsigned char *)s2;
	if (n == 0)
		return (0);
	i = 0;
	while ((i < n - 1) && s2p[i] == s1p[i] && s2p[i] && s1p[i])
		i++;
	return (s1p[i] - s2p[i]);
}
