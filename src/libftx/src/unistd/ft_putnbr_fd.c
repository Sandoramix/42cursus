#include "libft.h"
#include <unistd.h>

int	ft_putnbr_fd(long long n, int fd)
{
	const long long	orig_n = n;

	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (n >= 10)
		ft_putnbr_fd(n / 10, fd);
	ft_putchar_fd((n % 10) + '0', fd);
	return (ft_nbr_len(orig_n, 10));
}
